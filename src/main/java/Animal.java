//class Animal
public class Animal {
  private String name;
  private int rangerId;

  //constructor animal
  public Animal(String name, int rangerId) {
    this.name = name;
    this.rangerId = rangerId;
  }

  //getter methods
  public String getName() {
    return name;
  }

  public int getRangerId() {
    return rangerId;
  }
}
